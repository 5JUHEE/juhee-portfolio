# Topic: Python List Basics

"""
This file shows basic list usage in Python:
- creating a list
- accessing elements
- modifying elements
- checking length
"""

# create a list
lst = [1, 2, 3]

# access elements
print("First item:", lst[0])
print("Second item:", lst[1])

# modify an element
lst[0] = 10
print("Modified list:", lst)

# length of list
print("Length of list:", len(lst))
