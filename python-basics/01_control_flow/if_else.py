# Topic: Control Flow - If Statement

"""
This example demonstrates how to use if-else conditions in Python.
"""

a = 3
b = 5

if a > b:
    print("a is bigger than b")
else:
    print("b is bigger than a")

# Another example
num = 10

if num % 2 == 0:
    print("Even number")
else:
    print("Odd number")
